package chapter2;

public class CharacterEx1 {

	public static void main(String[] args) {

		char ch1 = 'A';
		System.out.println(ch1);
		System.out.println((int) ch1);

		char ch1Ex = 'Z';
		System.out.println(ch1Ex);
		System.out.println((int) ch1Ex);

		char ch2 = 66;
		System.out.println(ch2);

		char ch2Ex = 38;
		System.out.println(ch2Ex);

		int ch3 = 67;

		System.out.println(ch3);
		System.out.println((char) ch3);

		int ch3Ex = 97;

		System.out.println(ch3Ex);
		System.out.println((char) ch3Ex);

	}

}
